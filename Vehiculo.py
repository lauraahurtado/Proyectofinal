class Vehiculo:
    def __init__(self, nombre, modelo, clase_de_vehiculo, fabricante, costo_en_creditos, longitud, tripulacion, pasajeros, velocidad_maxima, capacidad_de_carga, consumibles, pilotos):
        self.nombre=nombre
        self.modelo=modelo
        self.clase_de_vehiculo=clase_de_vehiculo
        self.fabricante=fabricante
        self.costo_en_creditos=costo_en_creditos
        self.longitud=longitud
        self.tripulacion=tripulacion
        self.pasajeros=pasajeros
        self.velocidad_maxima=velocidad_maxima
        self.capacidad_de_carga=capacidad_de_carga
        self.consumibles=consumibles
        self.pilotos=pilotos